# Function to assign colors based on number of levels
assign_unique_colors <- function(char_vector, color_vector = NULL, palette = "Set2") {
  if(!require("RColorBrewer", quietly = TRUE))
    install.packages("RColorBrewer")
  library(RColorBrewer)

  unique_levels <- unique(char_vector)
  
  # If color_vector is not provided, create an empty one
  if (is.null(color_vector)) {
    color_vector <- vector("character", length = length(unique_levels))
    #names(color_vector) <- unique_levels
  }
  
  # Find levels without assigned colors
  unassigned_levels <- setdiff(unique_levels, names(color_vector))
  
  # Generate unique colors for unassigned levels
  if (length(unassigned_levels) < 9) {
    new_colors <- brewer.pal(n = length(unassigned_levels), name = palette)
  } else {
    palette <- colorRampPalette(c('#a6cee3','#1f78b4','#b2df8a','#33a02c','#fb9a99','#e31a1c','#fdbf6f','#ff7f00','#cab2d6','#6a3d9a','#ffff99','#b15928'))
    new_colors <- palette(n = length(unassigned_levels))
  }
  names(new_colors) <- unassigned_levels
  
  #  Update the existing color vector with new colors for previously unseen levels
  for (level in unassigned_levels) {
    color_vector[level] <- new_colors[level]
  }

  # remove any NAs
  color_vector <- color_vector[!is.na(names(color_vector))]

  # Return the updated color vector
  return(color_vector[unique(char_vector)])
}

main <- function() {
    # Create output directory if it doesn't exist
    dir.create(opt$output_dir, showWarnings = FALSE, recursive = TRUE)
    
    # Process stat files
    message("Processing samtools stats files...")
    results <- process_stat_files(opt$input_dir, opt$pattern)
    
    # Save extracted data
    message("Saving extracted statistics...")
    write.table(results$mapq, 
                file.path(opt$output_dir, "combined_quality_scores.tsv"),
                sep = "\t", row.names = FALSE, quote = FALSE)
    write.table(results$summary,
                file.path(opt$output_dir, "combined_summary_stats.tsv"),
                sep = "\t", row.names = FALSE, quote = FALSE)
    
    # Process alignment data for visualization
    message("Processing alignment statistics...")
    alignment_stats <- results$summary %>%
        group_by(sample) %>%
        mutate(across(where(is.character), as.numeric)) %>%
        select(c(sample, `sequences`, `reads mapped`, `reads unmapped`)) %>% 
        mutate(`align percent` = `reads mapped`/sequences*100) %>%
        mutate(timepoint = case_when(
            str_detect(sample, "_B_") ~ "baseline",
            str_detect(sample, "_M_") ~ "midpoint",
            str_detect(sample, "_E_") ~ "endpoint"
        ))

    write.csv(alignment_stats, file.path(opt$output_dir, "alignment-percentages.csv"))

    # Create visualization
    message("Generating alignment percentage histogram...")
    plot <- alignment_stats %>%
        ggplot(aes(x = align_percent)) + 
          geom_histogram(aes(fill = timepoint), position = "dodge", 
                        alpha = 0.5, binwidth = 5) +
          scale_fill_manual(
                  values = assign_unique_colors(alignment_stats$timepoint),
                  name = "Timepoint"  # Legend title
              ) +
          labs(
                  x = "Percentage of sequences aligned to WoL",
                  y = "Number of samples",
              )    
    # Save results
    ggsave(file.path(opt$output_dir, "alignment_percentage_histogram.png"),
           plot = plot, width = 10, height = 7.5, dpi = 300)
    
    message("Analysis complete! Results saved in: ", opt$output_dir)
}

# Execute main function
main()