# Function to extract and parse MAPQ lines
extract_mapq <- function(file) {
    # Read the file
    lines <- readLines(file)
    
    # Extract lines matching MAPQ pattern
    matches <- grep("^MAPQ", lines, value = TRUE)
    
    if(length(matches) == 0) {
        warning("No MAPQ lines found in file: ", basename(file))
        return(NULL)
    }
    
    # Split the lines and create a data frame
    result <- data.frame(
        do.call(rbind, strsplit(sub("^MAPQ\t", "", matches), "\t")),
        stringsAsFactors = FALSE
    )
    
    # Add sample name and proper column names
    result$sample <- basename(tools::file_path_sans_ext(file))
    colnames(result) <- c("MAPQ_score", "count", "sample")
    
    # Convert columns to appropriate types
    result$MAPQ_score <- as.numeric(result$MAPQ_score)
    result$count <- as.numeric(result$count)
    
    return(result)
}

# Function to extract and parse summary statistics
extract_summary <- function(file) {
    # Read the file
    lines <- readLines(file)
    
    # Extract lines matching SN pattern
    matches <- grep("^SN", lines, value = TRUE)
    
    if(length(matches) == 0) {
        warning("No summary statistics found in file: ", basename(file))
        return(NULL)
    }

    # Remove the pattern prefix and split the lines to create a data frame
    result <- separate(data.frame(matches=sub("^SN\\t", "", matches)), col = matches, into = c("stat", "count", "comment"), sep = "\\t" ) 
    
    # Add sample name and proper column names
    result$sample <- basename(tools::file_path_sans_ext(file))
    colnames(result) <- c("stat", "count", "comment", "sample")
    
    return(result)
}

# Function to process all files in directory
process_stat_files <- function(input_dir, pattern = "*.stats") {
    # Get list of stat files
    stat_files <- list.files(path = input_dir, 
                            pattern = pattern, 
                            full.names = TRUE)
    
    if (length(stat_files) == 0) {
        stop("No .stats files found in directory: ", input_dir)
    }
    
    # Process each file
    message("Processing ", length(stat_files), " stat files...")
    
    # Extract MAPQ scores
    mapq_results <- lapply(stat_files, function(file) {
        message("Extracting MAPQ scores from ", basename(file))
        extract_mapq(file)
    })
    
    # Extract summary statistics
    summary_results <- lapply(stat_files, function(file) {
        message("Extracting summary statistics from ", basename(file))
        extract_summary(file)
    })
    
    # Remove NULL results
    mapq_results <- Filter(Negate(is.null), mapq_results)
    summary_results <- Filter(Negate(is.null), summary_results)
    
    # Combine results
    mapq_combined <- do.call(rbind, mapq_results)
    summary_combined <- do.call(rbind, summary_results)
    summary_combined <- summary_combined %>% 
                            mutate(stat = str_remove_all(stat, "\\:")) %>%
                            pivot_wider(id_cols = sample, names_from = stat, values_from = count)
    
    return(list(mapq = mapq_combined, summary = summary_combined))
}

